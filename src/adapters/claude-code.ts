/**
 * Claude Code adapter.
 *
 * Emits the official Claude Code sub-agent format: a Markdown file with YAML
 * frontmatter in `.claude/agents/<name>.md`. Harness does not replace this
 * format — it generates it from the canonical `.subagents/*.yaml`.
 */
import YAML from "yaml";
import type {
  GeneratedFile,
  ManualContext,
  PlatformAdapter,
  ResolvedAgent,
} from "./types.js";

export const claudeCodeAdapter: PlatformAdapter = {
  id: "claude-code",
  displayName: "Claude Code",
  skillSupport: { verified: true },
  render({ agent, model, tools }: ResolvedAgent): GeneratedFile {
    const frontmatter: Record<string, unknown> = {
      name: agent.name,
      description: agent.description.trim(),
      tools: tools.join(", "),
    };
    // `inherit` means "use the parent session's model" — omit the key.
    if (model.effectiveTier !== "inherit") {
      frontmatter.model = model.model;
    }

    const fm = YAML.stringify(frontmatter, { lineWidth: 0 }).trimEnd();

    const body = [
      `<!-- GENERATED by harness from .subagents/${agent.name}.yaml — do not edit. -->`,
      "",
      agent.prompt.trim(),
    ].join("\n");

    return {
      relPath: `.claude/agents/${agent.name}.md`,
      contents: `---\n${fm}\n---\n\n${body}\n`,
    };
  },

  // Decision-routable skill: a thin launcher so the main agent can choose to
  // run this workflow. The sub-agent prompt stays the single source of truth.
  renderSkill({ agent, command }: ManualContext): GeneratedFile {
    const fm = YAML.stringify(
      { name: command, description: agent.description.trim() },
      { lineWidth: 0 },
    ).trimEnd();
    const body = [
      `<!-- GENERATED by harness from .subagents/${agent.name}.yaml — do not edit. -->`,
      "",
      `**Goal:** ${agent.goal.trim()}`,
      "",
      `When this skill is invoked, launch the \`${agent.name}\` sub-agent ` +
        `(\`.claude/agents/${agent.name}.md\`) to do the work in its own ` +
        `context, then report the result. Do not inline its prompt — dispatch ` +
        `the sub-agent so there is one source of truth.`,
      "",
      `Triggers (when the main agent should consider this): ${agent.triggers.join(", ")}.`,
    ].join("\n");
    return {
      relPath: `.claude/skills/${command}/SKILL.md`,
      contents: `---\n${fm}\n---\n\n${body}\n`,
    };
  },

  // Manual slash command: `/<command>` to invoke the workflow on demand.
  renderCommand({ agent, command }: ManualContext): GeneratedFile {
    const fm = YAML.stringify(
      { description: `Run the ${agent.name} workflow on demand` },
      { lineWidth: 0 },
    ).trimEnd();
    const body = [
      `<!-- GENERATED by harness from .subagents/${agent.name}.yaml — do not edit. -->`,
      "",
      `Launch the \`${agent.name}\` sub-agent ` +
        `(\`.claude/agents/${agent.name}.md\`) to:`,
      "",
      agent.goal.trim(),
    ].join("\n");
    return {
      relPath: `.claude/commands/${command}.md`,
      contents: `---\n${fm}\n---\n\n${body}\n`,
    };
  },
};
