/**
 * Cursor adapter.
 *
 * Verified (Cursor 2.4): sub-agents are Markdown files with YAML frontmatter in
 * .cursor/agents/<name>.md (name, description, model, readonly, is_background);
 * https://cursor.com/docs/subagents. Skills + commands live alongside.
 */
import type {
  GeneratedFile,
  ManualContext,
  PlatformAdapter,
  ResolvedAgent,
} from "./types.js";
import { launcherBody, withFrontmatter } from "./manual.js";

const agentRelPath = (name: string) => `.cursor/agents/${name}.md`;

export const cursorAdapter: PlatformAdapter = {
  id: "cursor",
  displayName: "Cursor",
  agentRelPath,
  // Verified (Cursor 2.4): Agent Skills (.cursor/skills/<name>/SKILL.md, with
  // disable-model-invocation to force manual) + Commands (.cursor/commands/<name>.md).
  // https://cursor.com/docs/skills
  skillSupport: {
    verified: true,
    note: ".cursor/skills/<cmd>/SKILL.md (decision-routed) + .cursor/commands/<cmd>.md (/<cmd>).",
  },
  render({ agent, model }: ResolvedAgent): GeneratedFile {
    // Verified Cursor sub-agent frontmatter. readonly when the agent has no
    // write capability; is_background follows parallelizable.
    const fm: Record<string, unknown> = {
      name: agent.name,
      description: agent.description.trim(),
      model: model.effectiveTier === "inherit" ? "inherit" : model.model,
      readonly: !agent.capabilities.includes("write"),
      is_background: agent.parallelizable,
    };
    const body = [
      `<!-- GENERATED by harness from .subagents/${agent.name}.yaml — do not edit. -->`,
      "",
      agent.prompt.trim(),
    ].join("\n");
    return { relPath: agentRelPath(agent.name), contents: withFrontmatter(fm, body) };
  },

  renderManual({ agent, command, wantsSkill, wantsCommand }: ManualContext): GeneratedFile[] {
    const files: GeneratedFile[] = [];
    if (wantsSkill) {
      files.push({
        relPath: `.cursor/skills/${command}/SKILL.md`,
        contents: withFrontmatter(
          { name: command, description: agent.description.trim() },
          launcherBody(agent, "skill"),
        ),
      });
    }
    if (wantsCommand) {
      // Cursor commands are plain markdown; the filename is the slash name.
      files.push({
        relPath: `.cursor/commands/${command}.md`,
        contents: `# ${command}\n\n${launcherBody(agent, "command")}\n`,
      });
    }
    return files;
  },
};
