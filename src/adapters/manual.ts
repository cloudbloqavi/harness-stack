/**
 * Shared helpers for rendering an agent's manual/skill entry points.
 *
 * Every generated skill/command/workflow file is a THIN launcher: it carries
 * the agent's goal and points at `.subagents/<name>.yaml` (the canonical,
 * platform-agnostic spec) as the single source of truth, rather than
 * duplicating the prompt.
 */
import YAML from "yaml";
import type { Subagent } from "../schema.js";

export function launcherBody(
  agent: Subagent,
  kind: "skill" | "command" | "workflow",
): string {
  return [
    `<!-- GENERATED by harness from .subagents/${agent.name}.yaml — do not edit. -->`,
    "",
    `**Goal:** ${agent.goal.trim()}`,
    "",
    `Thin ${kind} entry point for the \`${agent.name}\` sub-agent. Carry out ` +
      `the workflow exactly as specified in \`.subagents/${agent.name}.yaml\` ` +
      `— the canonical, platform-agnostic spec and the single source of truth. ` +
      `Do not duplicate its prompt here.`,
  ].join("\n");
}

/** Wrap a markdown body in a YAML frontmatter block. */
export function withFrontmatter(
  frontmatter: Record<string, unknown>,
  body: string,
): string {
  const fm = YAML.stringify(frontmatter, { lineWidth: 0 }).trimEnd();
  return `---\n${fm}\n---\n\n${body}\n`;
}
